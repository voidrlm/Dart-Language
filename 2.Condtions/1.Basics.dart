void main() {
  int num1 = 10;
  int num2 = 0;

  //
  //normal if,else if,else
  if (num2 > num2) {
    print('num2 is greater');
  } else if (num1 == num2) {
    print('equal');
  } else {
    print('num2 is smaller');
  }
  //
  // ternary
  var check = (num2 > num2) ? 'num2 is greater' : 'else';
  print(check);
}
